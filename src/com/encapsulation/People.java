package com.encapsulation;

public class People {

    //Read Only Encapsulation

    private String country = "Bangladesh";
    private String language = "Bangla";
    private int hand = 2;

   //getter Method


    public String getCountry() {
        return country;
    }

    public String getLanguage() {
        return language;
    }

    public int getHand() {
        return hand;
    }
}
