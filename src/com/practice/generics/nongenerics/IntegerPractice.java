package com.practice.generics.nongenerics;

public class IntegerPractice {

    private int age, roll;

    public IntegerPractice(int age, int roll) {
        this.age = age;
        this.roll = roll;
    }

    public int getAge() {
        return age;
    }

    public int getRoll() {
        return roll;
    }
}
